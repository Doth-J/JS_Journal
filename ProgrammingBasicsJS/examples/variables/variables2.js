// NUMBER
let a = 5; // Number
let b = 2.345; // Number
console.log(a,typeof a);
console.log(b,typeof b);

// STRING
let str1 = "hello"; // String
let str2 = 'world'; // String
let message = str1 + " " + str2; // String
console.log(str1,typeof str1);
console.log(str2,typeof str2);
console.log(message, typeof message);

// BOOLEAN
let truthy = true; // Boolean 
let falsy = false; // Boolean
console.log(truthy,typeof truthy);
console.log(falsy,typeof falsy);

// ARRAY
let array = [1,2.5,true,"astring",'anotherone']; // Array
console.log(array,typeof array);

// OBJECT
let object = {number:123,message:"hello world"}; // Object
console.log(object,typeof object);

// FUNCTION
let func = function getMessage(){return "hello world";}; // Function
console.log(func(),typeof func);

// NULL
let nully = null; // Null
console.log(nully,typeof nully);

// UNDEFINED
let newvariable; // Undefined
console.log(newvariable,typeof newvariable);